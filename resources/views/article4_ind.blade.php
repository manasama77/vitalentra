<p>
    <b>Vitalentra Group International (VGI)</b>, perusahaan distribusi yang bergerak di bidang kesehatan dan kecantikan berbasis alami, resmi
    diluncurkan
    hari ini, Selasa, 9 September 2025 di Jakarta. Kehadiran VGI berangkat dari tantangan kesehatan yang dihadapi oleh bangsa Indonesia, salah satunya
    adalah semakin meningkatnya jumlah penderita diabetes yang kini telah mencapai lebih dari 20,4 juta orang dewasa, atau sekitar 11,3% populasi
    dewasa menurut data International Diabetes Federation (2024).
</p>

<p>
    Selain itu, data Kementerian Kesehatan RI pada tahun yang sama bahkan mencatat hampir 13% penduduk, atau sekitar 35,8 juta orang, hidup dengan
    diabetes. Fakta ini menunjukkan bahwa Indonesia termasuk salah satu negara dengan beban diabetes tertinggi di dunia.
</p>

<p>
    Vitalentra Group International hadir sebagai mitra bisnis BP Group, perusahaan yang dikenal dengan portofolio produk <i>wellness</i> dan suplemen
    alami
    di Indonesia. Melalui kemitraan ini, VGI mendistribusikan rangkaian produk unggulan seperti Steffi (gula cair stevia), British Propolis, BP
    Norway, dan Belgie Skincare, tidak hanya ke pasar Indonesia tetapi juga mancanegara. Semua produk yang kami pasarkan dikembangkan dengan standar
    kualitas tinggi dari BP Group, dimana kami juga terus memastikan produk-produk tersebut dapat menjangkau lebih banyak orang dan kalangan di
    Indonesia dengan dukungan jaringan distribusi yang kuat.
</p>

<p>
    Steffi menjadi salah satu produk utama yang ditawarkan oleh VGI. Sebagai pemanis cair alami yang dibuat dari daun stevia, Steffi menawarkan cara
    baru bagi keluarga Indonesia untuk tetap menikmati rasa manis tanpa risiko gula berlebih. CEO Vitalentra Group International, M. Nuzullaiman,
    menjelaskan bahwa <i>wellness</i> tidak harus dimulai dengan perubahan besar, tetapi cukup dengan langkah kecil dan sederhana yang konsisten.
<blockquote>“Wellness tidak harus rumit. Dimulai dari langkah kecil, seperti mengganti gula dengan Steffi, kita bisa mengurangi risiko diabetes dan
    penyakit terkait lainnya. Dengan pilihan yang sederhana ini, kami ingin membantu dan mengajak lebih banyak orang menjalani hidup yang lebih sehat
    dan tetap bahagia,”</blockquote> ujarnya.
</p>

<p>
    Acara peluncuran hari ini semakin istimewa dengan keterlibatan pasangan publik figur Mario Irwinsyah dan Ratu Anandita, yang keduanya menjabat
    sebagai Komisaris Vitalentra Group International dan sekaligus menjadi Wellness Advocates perusahaan. Peran ini menunjukkan komitmen mereka tidak
    hanya sebagai wajah publik, tetapi juga sebagai bagian dari penggerak visi perusahaan.
<blockquote>“Dulu kami sulit lepas dari gula. Tapi setelah mengenal Steffi, rasanya kami menemukan jalan tengah yang lebih sehat. Jadi kami tetap bisa
    menikmati manis, tapi tanpa rasa bersalah. Sekarang,anak-anak pun ikut terbiasa, dan itu membuat kami merasa lebih tenang sebagai orang tua,”
</blockquote> ujar Mario dan Ratu.
</p>

<p>
    Vitalentra Group International lahir dengan visi besar untuk menjadikan <i>wellness</i> alami sebagai gaya hidup masyarakat Indonesia. Visi
    tersebut diwujudkan melalui misi menghadirkan produk kesehatan dan kecantikan berbasis bahan alami yang aman, berkualitas, dan terjangkau;
    mendorong pola hidup sehat dengan mengganti kebiasaan berisiko tinggi seperti konsumsi gula berlebih; menginspirasi masyarakat untuk mengambil
    langkah kecil yang membawa perubahan besar bagi kesehatan dan kebahagiaan jangka panjang; serta memperluas distribusi produk ke pasar lokal dan
    internasional sehingga <i>wellness</i> alami dapat menjadi bagian dari solusi global.
</p>

<p>
    Dengan dukungan BP Group, VGI memperkuat fondasi bisnis melalui standar global dalam distribusi dan jaminan kualitas produk. Kemitraan ini tidak
    hanya memperkuat posisi VGI di pasar domestik, tetapi juga membuka jalan bagi ekspansi internasional.
<blockquote>“Kami ingin Indonesia dikenal bukan hanya sebagai konsumen, tetapi juga sebagai pelopor produk wellness alami di tingkat global,”
</blockquote> tambah M. Nuzullaiman.
</p>

<p>
    Dengan semangat <i>“Natural Wellness for Everyone”</i>, peluncuran Vitalentra Group International bukan sekadar perkenalan sebuah perusahaan baru,
    tetapi juga sebuah Gerakan yang mengajak masyarakat untuk memulai langkah kecil menuju hidup yang lebih sehat, lebih alami, dan lebih bermakna.
</p>

