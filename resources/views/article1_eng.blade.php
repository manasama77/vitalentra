<h2>What Is Stevia?</h2>

<p>Stevia is a natural sweetener derived from the leaves of the <em>Stevia rebaudiana</em> plant, which has long been
    used by South American communities to sweeten tea and traditional herbal drinks. Today, stevia is known as a
    zero-calorie natural sweetener that is 200–300 times sweeter than sugar, without the negative effects.</p>

<ol>
    <li>
        <strong>Zero Calories, Zero Guilt</strong><br>
