<p>
    <b>Vitalentra Group International (VGI)</b>, a distribution company operating in the field of natural-based health and beauty, was officially
    launched
    today, Tuesday, September 9, 2025 in Jakarta. VGI's presence stems from health challenges faced by the Indonesian nation, one of which
    is the increasing number of diabetes sufferers which has now reached more than 20.4 million adults, or around 11.3% of the adult population
    according to data from the International Diabetes Federation (2024).
</p>

<p>
    Moreover, data from the Indonesian Ministry of Health in the same year even recorded that nearly 13% of the population, or around 35.8 million
    people, live with
    diabetes. This fact shows that Indonesia is among the countries with the highest diabetes burden in the world.
</p>

<p>
    Vitalentra Group International is present as a business partner of BP Group, a company known for its portfolio of <i>wellness</i> and natural
    supplement
    products
    in Indonesia. Through this partnership, VGI distributes a range of flagship products such as Steffi (liquid stevia sugar), British Propolis, BP
    Norway, and Belgie Skincare, not only to the Indonesian market but also internationally. All products we market are developed with high
    quality standards from BP Group, where we also continue to ensure these products can reach more people and communities in
    Indonesia with the support of a strong distribution network.
</p>

<p>
    Steffi is one of the main products offered by VGI. As a natural liquid sweetener made from stevia leaves, Steffi offers a
    new way for Indonesian families to continue enjoying sweetness without the risk of excess sugar. CEO of Vitalentra Group International, M.
    Nuzullaiman,
    explained that <i>wellness</i> doesn't have to start with big changes, but simply with small and simple steps that are consistent.
<blockquote>"Wellness doesn't have to be complicated. Starting from small steps, like replacing sugar with Steffi, we can reduce the risk of diabetes
    and
    other related diseases. With this simple choice, we want to help and invite more people to live healthier lives
    while staying happy,"</blockquote> he said.
</p>

<p>
    Today's launch event became even more special with the involvement of public figure couple Mario Irwinsyah and Ratu Anandita, both of whom serve
    as Commissioners of Vitalentra Group International and simultaneously become the company's Wellness Advocates. This role shows their commitment
    not
    only as public faces, but also as part of the driving force behind the company's vision.
<blockquote>"We used to have difficulty giving up sugar. But after getting to know Steffi, we felt we found a healthier middle ground. So we can still
    enjoy sweetness, but without guilt. Now, the children have also gotten used to it, and that makes us feel more at ease as parents,"
</blockquote> said Mario and Ratu.
</p>

<p>
    Vitalentra Group International was born with a big vision to make natural <i>wellness</i> a lifestyle for Indonesian society. This vision
    is realized through the mission of providing health and beauty products based on natural ingredients that are safe, quality, and affordable;
    encouraging healthy lifestyles by replacing high-risk habits such as excessive sugar consumption; inspiring society to take
    small steps that bring big changes for long-term health and happiness; as well as expanding product distribution to local and
    international markets so that natural <i>wellness</i> can become part of a global solution.
</p>

<p>
    With support from BP Group, VGI strengthens its business foundation through global standards in distribution and product quality assurance. This
    partnership not
    only strengthens VGI's position in the domestic market, but also opens the way for international expansion.
<blockquote>"We want Indonesia to be known not only as a consumer, but also as a pioneer of natural wellness products at the global level,"
</blockquote> added M. Nuzullaiman.
</p>

<p>
    With the spirit of <i>"Natural Wellness for Everyone"</i>, the launch of Vitalentra Group International is not just an introduction of a new
    company,
    but also a Movement that invites society to start small steps towards a healthier, more natural, and more meaningful life.
</p>

