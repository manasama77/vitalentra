<p>
    Kita semua suka yang manis. Mulai dari minuman favorit sampai camilan sehari-hari. Tapi tahukah kamu, konsumsi gula
    biasa (sukrosa) dalam jangka panjang bisa membawa dampak serius bagi kesehatan?
</p>

<p>
    Berikut adalah beberapa efek negatif konsumsi gula berlebih yang sering tidak disadari:
</p>

<ol>
    <li>
        <strong>Meningkatkan Risiko Diabetes Tipe 2</strong><br>
        Kebiasaan mengonsumsi makanan dan minuman tinggi gula menyebabkan lonjakan kadar gula darah. Jika terus-menerus
        terjadi, tubuh bisa mengalami resistensi insulin yang berujung pada diabetes tipe 2.
    </li>
    <li>
        <strong>Membebani Jantung dan Pembuluh Darah</strong><br>
        Gula berlebih dapat memicu tekanan darah tinggi, peradangan, dan peningkatan kolesterol jahat (LDL) â€” semua
        faktor risiko penyakit jantung.
    </li>
    <li>
        <strong>Memicu Kenaikan Berat Badan dan Obesitas</strong><br>
        Gula mengandung kalori tinggi namun minim nutrisi. Dalam jumlah besar, ia bisa membuat tubuh menyimpan lemak
        berlebih, terutama di bagian perut.
    </li>
    <li>
        <strong>Mempengaruhi Fungsi Otak dan Mood</strong><br>
        Gula bisa memberi efek cepat seperti "happy boost", tapi juga diikuti dengan crash yang membuatmu cepat lelah,
        mudah marah, dan sulit fokus.
    </li>
    <li>
        <strong>Merusak Kesehatan Gigi dan Kulit</strong><br>
        Gula memberi makan bakteri penyebab gigi berlubang. Selain itu, kelebihan gula dalam darah bisa memicu jerawat
        dan penuaan dini pada kulit.
    </li>
</ol>

